package com.example.demo.vo;

public class Buisness {

    private String business_type;
    private String business_state;
    private String business_distribution;
    private String business_remark;

    public Buisness(){

    }

    public String getBusiness_type() {
        return business_type;
    }

    public void setBusiness_type(String business_type) {
        this.business_type = business_type;
    }

    public String getBusiness_state() {
        return business_state;
    }

    public void setBusiness_state(String business_state) {
        this.business_state = business_state;
    }

    public String getBusiness_distribution() {
        return business_distribution;
    }

    public void setBusiness_distribution(String business_distribution) {
        this.business_distribution = business_distribution;
    }

    public String getBusiness_remark() {
        return business_remark;
    }

    public void setBusiness_remark(String business_remark) {
        this.business_remark = business_remark;
    }
}
